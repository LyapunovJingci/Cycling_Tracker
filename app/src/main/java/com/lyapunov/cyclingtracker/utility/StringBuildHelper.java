package com.lyapunov.cyclingtracker.utility;

public class StringBuildHelper {

    public String buildString(String a, String b) {
        StringBuilder builder = new StringBuilder();
        builder.append(a);
        builder.append(b);
        return builder.toString();
    }
    public String buildString(String a, String b, String c) {
        StringBuilder builder = new StringBuilder();
        builder.append(a);
        builder.append(b);
        builder.append(c);
        return builder.toString();
    }
    public String buildString(String a, String b, String c, String d) {
        StringBuilder builder = new StringBuilder();
        builder.append(a);
        builder.append(b);
        builder.append(c);
        builder.append(d);
        return builder.toString();
    }
    public String buildString(String a, String b, String c, String d, String e) {
        StringBuilder builder = new StringBuilder();
        builder.append(a);
        builder.append(b);
        builder.append(c);
        builder.append(d);
        builder.append(e);
        return builder.toString();
    }
    public String buildString(String a, String b, String c, String d, String e, String f) {
        StringBuilder builder = new StringBuilder();
        builder.append(a);
        builder.append(b);
        builder.append(c);
        builder.append(d);
        builder.append(e);
        builder.append(f);
        return builder.toString();
    }

}
